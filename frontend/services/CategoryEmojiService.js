export function getCategoryEmojis(prefix) {
  return CATEGORY_EMOJIS[prefix] || [];
}

export function getEmojiForGeneratedId(prefix, num) {
  const list = getCategoryEmojis(prefix);
  if (!list.length || !Number.isFinite(num)) return null;
  const idx = (num - 1) % list.length;
  return list[idx] || null;
}

export function getEmojiForId(id) {
  if (!id || typeof id !== 'string') return null;
  const [rawPrefix, rawNum] = id.split('_');
  const num = Number(rawNum);
  // Sadece otomatik üretilen kategori ID'leri: emoji_, animals_, nature_, vehicles_
  if (['emoji','animals','nature','vehicles'].includes(rawPrefix)) {
    return getEmojiForGeneratedId(rawPrefix, num);
  }
  return null;
}